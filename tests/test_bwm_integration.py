class TestSpatialQueries(unittest.TestCase):

    def setUp(self):
        self.wm = WOKParser.from_bytes(_simple_wok(), name="spatial")
        # simple.wok walkable area spans (0,0)→(2,2) approx
        # non-walkable spans (2,0)→(4,2)

    def test_height_at_walkable_returns_float(self):
        z = self.wm.height_at(0.5, 0.5)
        self.assertIsNotNone(z)
        self.assertAlmostEqual(z, 0.0, places=4)

    def test_height_at_nonwalk_area_returns_none(self):
        """Query over non-walkable face should return None (walkable_only=True)."""
        z = self.wm.height_at(3.0, 1.0)
