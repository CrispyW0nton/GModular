class TestERFWOKPackaging(unittest.TestCase):
    """Test packing WOK data into an ERF archive and reading it back."""

    def test_wok_in_erf_round_trip(self):
        """Pack a WOK into an ERF, unpack and re-parse it."""
        from gmodular.formats.archives import ERFWriter, ERFReaderMem

        wm = WOKParser.from_bytes(_simple_wok(), name="packed")
        wok_bytes = WOKWriter(wm).to_bytes()

        # Pack into ERF
        writer = ERFWriter()
        writer.add_resource("packed", "wok", wok_bytes)
        erf_bytes = writer.to_bytes()
        self.assertGreater(len(erf_bytes), 0)

        # Unpack
        reader = ERFReaderMem(erf_bytes)
        result = reader.get_resource("packed", "wok")
        self.assertIsNotNone(result)

        # Re-parse
        rt = WOKParser.from_bytes(result, name="packed_rt")
        self.assertEqual(rt.face_count, wm.face_count)
        self.assertEqual(rt.walkable_face_count, wm.walkable_face_count)

    def test_multiple_woks_in_erf(self):
        """Multiple room WOKs can be stored in one ERF."""
        from gmodular.formats.archives import ERFWriter, ERFReaderMem

        wm1 = WOKParser.from_bytes(_simple_wok(), name="room1")
        wm2 = WOKParser.from_bytes(_grid_wok(),   name="room2")

        writer = ERFWriter()
        writer.add_resource("room1", "wok", WOKWriter(wm1).to_bytes())
        writer.add_resource("room2", "wok", WOKWriter(wm2).to_bytes())
        erf_bytes = writer.to_bytes()

        reader = ERFReaderMem(erf_bytes)
        r1 = WOKParser.from_bytes(reader.get_resource("room1", "wok"), name="r1")
        r2 = WOKParser.from_bytes(reader.get_resource("room2", "wok"), name="r2")

        self.assertEqual(r1.face_count, 4)
        self.assertEqual(r2.face_count, 32)


