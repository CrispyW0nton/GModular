class TestBWMRoundTrip(unittest.TestCase):

    def _round_trip(self, wm: WalkMesh) -> WalkMesh:
        data = WOKWriter(wm).to_bytes()
        return WOKParser.from_bytes(data, name=wm.name + "_rt")

    def test_simple_round_trip_face_count(self):
        wm = WOKParser.from_bytes(_simple_wok(), name="rt_simple")
        rt = self._round_trip(wm)
        self.assertEqual(rt.face_count, wm.face_count)

    def test_simple_round_trip_walkable_count(self):
        wm = WOKParser.from_bytes(_simple_wok(), name="rt_simple")
        rt = self._round_trip(wm)
        self.assertEqual(rt.walkable_face_count, wm.walkable_face_count)

    def test_simple_round_trip_materials(self):
        wm = WOKParser.from_bytes(_simple_wok(), name="rt_simple")
        rt = self._round_trip(wm)
        orig_mats = sorted(f.material for f in wm.faces)
        rt_mats   = sorted(f.material for f in rt.faces)
        self.assertEqual(orig_mats, rt_mats)

    def test_simple_round_trip_has_aabbs(self):
        wm = WOKParser.from_bytes(_simple_wok(), name="rt_simple")
        rt = self._round_trip(wm)
        self.assertGreater(len(rt.aabbs), 0)

