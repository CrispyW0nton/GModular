# ═════════════════════════════════════════════════════════════════════════════

class TestSimpleWOKParsing(unittest.TestCase):

    def setUp(self):
        self.wm = WOKParser.from_bytes(_simple_wok(), name="test_simple")

    # --- basic counts ---

    def test_name(self):
        self.assertEqual(self.wm.name, "test_simple")

    def test_total_face_count(self):
        self.assertEqual(self.wm.face_count, 4)

    def test_walkable_face_count(self):
        self.assertEqual(self.wm.walkable_face_count, 2)

    def test_non_walkable_face_count(self):
        self.assertEqual(len(self.wm.non_walkable_faces), 2)

    def test_has_aabb_nodes(self):
        self.assertGreater(len(self.wm.aabbs), 0)

    def test_position_is_origin(self):
        self.assertAlmostEqual(self.wm.position[0], 0.0, places=5)
        self.assertAlmostEqual(self.wm.position[1], 0.0, places=5)
        self.assertAlmostEqual(self.wm.position[2], 0.0, places=5)

    # --- face geometry ---

    def test_faces_are_walkface_instances(self):
        for f in self.wm.faces:
            self.assertIsInstance(f, WalkFace)

    def test_walkable_faces_have_dirt_material(self):
        for f in self.wm.walkable_faces:
            self.assertEqual(f.material, 0)  # Dirt

    def test_nonwalk_faces_have_nonwalk_material(self):
        for f in self.wm.non_walkable_faces:
            self.assertEqual(f.material, 6)  # NonWalk

    def test_face_vertices_are_tuples(self):
        f = self.wm.faces[0]
        self.assertEqual(len(f.v0), 3)
        self.assertEqual(len(f.v1), 3)
        self.assertEqual(len(f.v2), 3)

    def test_face_normal_is_unit_vector(self):
        for f in self.wm.faces:
            n = f.normal
            length = math.sqrt(n[0]**2 + n[1]**2 + n[2]**2)
            self.assertAlmostEqual(length, 1.0, places=5,
                                   msg=f"Normal not unit: {n}")

    def test_face_upward_normals(self):
        """Flat walkmesh faces should have upward-facing normals (nz > 0)."""
        for f in self.wm.faces:
            self.assertGreater(f.normal[2], 0.5,
                               msg=f"Face normal not upward: {f.normal}")

    def test_face_center_within_bounds(self):
        """Each face center must lie within the walkmesh bounding box."""
        bb_min, bb_max = self.wm.bounds()
        for f in self.wm.faces:
            cx, cy, cz = f.center
            self.assertGreaterEqual(cx, bb_min[0] - 0.01)
            self.assertLessEqual(cx, bb_max[0] + 0.01)
            self.assertGreaterEqual(cy, bb_min[1] - 0.01)
            self.assertLessEqual(cy, bb_max[1] + 0.01)

    # --- walkability property ---

    def test_walkface_walkable_property_true(self):
        f = WalkFace(v0=(0,0,0), v1=(1,0,0), v2=(0,1,0), material=0)
        self.assertTrue(f.walkable)

    def test_walkface_walkable_property_false(self):
        f = WalkFace(v0=(0,0,0), v1=(1,0,0), v2=(0,1,0), material=6)
        self.assertFalse(f.walkable)


